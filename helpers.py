def save_credentials_to_json(credentials):
    credentials_dict = credentials_to_dict(credentials)
    with open('saved_credentials.json', 'w') as json_file:
        json.dump(credentials_dict, json_file)

def load_credentials_from_json():
    try:
        with open('saved_credentials.json', 'r') as json_file:
            credentials_dict = json.load(json_file)
            credentials = Credentials(**credentials_dict)
            return credentials
    except FileNotFoundError:
        return None